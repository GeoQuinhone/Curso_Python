#Conversão de tipos, coerção
#type convertion, typecasting, coercion
#é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool

print(1 + 1)
print(int('1') + 1)
print(float('1') + 1)
print('Geovane' + 'Quinhone') #concatenar
print('1', type('1')) 
print(int('2'), type(int('2'))) #coerção de string para int 
print(str(11) + 'Geovane')
