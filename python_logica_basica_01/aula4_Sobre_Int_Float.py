#Tipos int e float
#int -> Número inteiro
#O tipo int representa qualquer número
#positivo ou negatovi. int sem sinal é considerado 
#positivo.
print(11)
print (-11)
print(0)
#------------------------------------------------------------------------

#float -> Número com ponto flutuante
#O tipo float representa qualquer número
#positivo ou negatovi com ponto flutuante
#float sem sinaç é considerado positivo.
#sempre utilizar . no float, pois a , faz outra função
print(1.1, 10.11) 
print(0.0, -1.5)
#------------------------------------------------------------------------

