"""
Docstring for python_logica_basica_01.aula19_Sobre_comparadores

    Operadores de Comparação (relacionais)
OP - - Significado -- Exemplo (true)
> -- maior -- 2 > 1
>= -- maior ou igual -- 2 >= 2
< -- menor -- 1 < 2
<= -- menor ou igual -- 2 <= 2
== -- igual -- 'a' == 'a'
!= -- diferente -- 'a' != 'b'
"""

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)